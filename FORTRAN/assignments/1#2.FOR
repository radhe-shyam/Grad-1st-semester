C     PROGRAM TO FIND THE AREA AND THE PERIMETER OF A SQUARE
      WRITE(*,*)'ENTER THE LENGTH OF A SIDE'
      READ(*,*)R
      AREA=R*R
      PERIMETER=4*R
      WRITE(*,*)'THE AREA IS',AREA
      WRITE(*,*)'THE PERIMETER IS',PERIMETER
      WRITE(*,*)'CREATED BY RADHE SHYAM JANGID'
      STOP
      END
