C	PROGRAM FOR MULTIPLICATION OF TWO MATRICES
	DIMENSION M(5,5)
	DIMENSION N(5,5)
	DIMENSION O(5,5)
	WRITE(*,*)'ENTER THE NO OF ROWS FOR 1st MATRIX'
	READ(*,*)IR1
	WRITE(*,*)'ENTER THE NO OF COLS FOR 1st MATRIX'
	READ(*,*)IC1
	WRITE(*,*)'ENTER THE NO OF ROWS FOR 2nd MATRIX'
	READ(*,*)IR2
	WRITE(*,*)'ENTER THE NO OF COLS FOR 2nd MATRIX'
	READ(*,*)IC2
	DO 10 I=1,IR1,1
	DO 20 J=1,IC1,1
	READ(*,*)M(I,J)
   20	CONTINUE
   10	CONTINUE
	DO 30 I=1,IR2,1
	DO 40 J=1,IC2,1
	READ(*,*)N(I,J)
   40	CONTINUE
   30	CONTINUE
	IF (IC1.EQ.IR2) THEN
	DO 50 I=1,IR1,1
	DO 60 J=1,IC2,1
	O(I,J)=0
	DO 70 K=1,IC1,1
   70	O(I,J)=O(I,J)+(M(I,K)*B(K,J))
   60	CONTINUE
   50	CONTINUE
	ELSE
	WRITE(*,*)'THE MULTIPLICATION IS NOT POSSIBLE'
	ENDIF
	STOP
	END