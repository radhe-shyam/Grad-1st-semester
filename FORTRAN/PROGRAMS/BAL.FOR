      REAL INTRT,MIN
      INTEGER SERIAL
      DO 100 I=1,2
        READ(*,*) SERIAL,RAT,BAL,YRS,MIN
        BAL=BAL+INTRT(RAT,BAL,YRS,MIN)
        WRITE(*,*)SERIAL,RAT,BAL,YRS,MIN
100   CONTINUE
      STOP
      END

      REAL FUNCTION INTRT(RATE,BALNCE,YEARS,MINBAL)
      REAL MINBAL
        IF(BALANCE .GT. MINBAL)THEN
                INTRT=BALNCE*RATE/100.*YEARS
        ELSE
                INTRT=0
      ENDIF
      RETURN
      END
