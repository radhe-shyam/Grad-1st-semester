       WRITE(*,*)'ENTER THE ANY NUMBER N'
       READ(*,*)N
       "IF IS REMAINDER OF N.MOD.2 IS ZERO THEN PERFORM STEP4 OTHER WISE PERFORM STEP 5"  "
       WRITE(*,*)'N IS EVEN'
       ELSE
       WRITE(*,*)'N IS ODD'
       ENDIF
       STOP
       END



